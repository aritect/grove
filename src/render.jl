const INDEX_BANNER = "<!-- AUTO-GENERATED by grove. Do not edit. Run `grove render` to refresh. -->"

function render_index(st::State)::String
    io = IOBuffer()
    println(io, INDEX_BANNER)
    println(io)
    println(io, "# Development dashboard")
    println(io)
    render_goals!(io, st)
    render_work!(io, st)
    render_decisions!(io, st)
    render_questions!(io, st)
    render_assumptions!(io, st)
    render_retros!(io, st)
    render_artifacts!(io, st)
    render_graph!(io, st)
    String(take!(io))
end

function question_targets(st::State, q::Node)::Vector{String}
    sort!(collect(Set([e.to for e in st.edges if e.label === :asks && e.from == q.id])))
end

function assumption_tests(st::State, b::Node)::Vector{String}
    sort!(collect(Set([e.to for e in st.edges if e.label === :tests && e.from == b.id])))
end

function assumption_targets(st::State, b::Node)::Vector{String}
    sort!(collect(Set([e.to for e in st.edges if e.label === :targets && e.from == b.id])))
end

function decision_supersedes(st::State, d::Node)::Vector{String}
    sort!(collect(Set([e.to for e in st.edges if e.label === :supersedes && e.from == d.id])))
end

function render_goals!(io, st)
    rows = listnodes(st, :g)
    isempty(rows) && return
    println(io, "## Goals")
    println(io)
    println(io, "| ID | Outcome | Fitness function | Status |")
    println(io, "| --- | --- | --- | --- |")
    for n in rows
        f = goal_fitness_table_cell(n)
        println(io, "| ", n.id, " | ", n.title, " | ", f, " | ", n.status, " |")
    end
    println(io)
end

function render_work!(io, st)
    rows = listnodes(st, :w)
    isempty(rows) && return
    cp = Set(critical_path(st))
    println(io, "## Work items")
    println(io)
    println(io, "| ID | Type | Title | Goals | Cynefin | DoR | Status | Critical |")
    println(io, "| --- | --- | --- | --- | --- | --- | --- | --- |")
    for n in rows
        gs = join(get(n.fields, :goals, String[]), ", ")
        do_r = dor(st, n) ? "⊤" : "⊥"
        crit = n.id in cp ? "★" : ""
        println(io, "| ", n.id, " | ", n.type, " | ", n.title, " | ", gs, " | ",
                n.cynefin, " | ", do_r, " | ", n.status, " | ", crit, " |")
    end
    println(io)
end

